let currentLang = localStorage.getItem('lang') || 'pt';

function t(key) {
    return translations[currentLang][key] || key;
}

function applyTranslations() {
    document.documentElement.lang = currentLang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.innerHTML = t(key);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    applyTranslations();
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();

    // Ensure language selector reflects current language
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
        langSelect.value = currentLang;
        langSelect.addEventListener('change', (event) => {
            setLanguage(event.target.value);
        });
    }

    const challengePhrases = {
        pt: ['Missão: Elimine 5 bugs em 2 minutos', 'Combo x4 ativa: acerte sem errar', 'Modo noturno ativo: +30% de XP'],
        en: ['Mission: Eliminate 5 bugs in 2 minutes', 'Combo x4 active: hit streak', 'Night mode on: +30% XP'],
        es: ['Misión: Eliminar 5 bugs en 2 minutos', 'Combo x4 activo: golpea sin fallar', 'Modo nocturno activo: +30% XP']
    };

    let currentPhraseIndex = 0;
    const challengeElement = document.getElementById('challengeTitle');
    setInterval(() => {
        if (!challengeElement) return;
        const phrases = challengePhrases[currentLang] || challengePhrases['pt'];
        challengeElement.textContent = phrases[currentPhraseIndex % phrases.length];
        currentPhraseIndex += 1;
    }, 4200);
});
